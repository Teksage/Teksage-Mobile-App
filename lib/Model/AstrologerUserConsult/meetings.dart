class Meeting {
  final String name;
  final String date;
  final String initials;
  final int meetingId;
  final String fullName;
  final String meetingLink;

  Meeting( {
    required this.name,
    required this.date,
    required this.initials,
    required this.meetingId,
    required this.fullName,
    required this.meetingLink,
  });
}
